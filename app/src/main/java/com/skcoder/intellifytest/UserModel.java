package com.skcoder.intellifytest;

public class UserModel {
    String name, city,gender, age;

    public UserModel() {
    }

    public UserModel(String name, String city, String gender, String age) {
        this.name = name;
        this.city = city;
        this.gender = gender;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
